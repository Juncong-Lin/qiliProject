// Helper function to get printer by ID
export function getPrinterById(id) {
  for (const category in printerProducts) {
    const printer = printerProducts[category].find(p => p.id === id);
    if (printer) return printer;
  }
  return null;
}

// Helper function to get all printers
export function getAllPrinters() {
  const allPrinters = [];
  for (const category in printerProducts) {
    allPrinters.push(...printerProducts[category]);
  }
  return allPrinters;
}
